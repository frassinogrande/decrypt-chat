#!/usr/bin/env node
/**
 * Auto-generates TypeScript declarations for $paraglide/messages and $paraglide/runtime.
 *
 * Reads the compiled src/paraglide/messages.js and produces src/paraglide.d.ts so that
 * TypeScript always sees every message key.  Runs automatically before `npm run check`
 * via the "precheck" script, so declarations can never fall behind the compiled output.
 *
 * Also deletes src/paraglide-settings-menu.d.ts if it still exists (replaced by this script).
 */

import { readFileSync, writeFileSync, existsSync, unlinkSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import prettier from 'prettier';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const messagesPath = join(root, 'src/paraglide/messages.js');
const outputPath = join(root, 'src/paraglide.d.ts');
const legacySettingsPath = join(root, 'src/paraglide-settings-menu.d.ts');

if (!existsSync(messagesPath)) {
    console.warn(
        'src/paraglide/messages.js not found — run `npm run dev` or `npm run build` first to compile paraglide messages.'
    );
    process.exit(0);
}

const content = readFileSync(messagesPath, 'utf8');

// Split on JSDoc block starts so each element is one function block
const blocks = content.split(/(?=\/\*\*)/).filter((b) => b.includes('export const'));

const declarations = [];

for (const block of blocks) {
    const exportMatch = block.match(/export const (\w+)\s*=/);
    if (!exportMatch) continue;
    const name = exportMatch[1];

    // @param {{ key: NonNullable<unknown> }} params  →  (args: { key: string }) => string
    // @param {{}} params                             →  () => string
    const paramMatch = block.match(/@param \{(\{[^}]*\})\} params/);
    if (!paramMatch || paramMatch[1].trim() === '{}') {
        declarations.push(`    export const ${name}: () => string;`);
        continue;
    }

    // Extract property names from the inner object type, e.g. { name: NonNullable<unknown> }
    const innerType = paramMatch[1];
    const props = [...innerType.matchAll(/(\w+):\s*NonNullable<unknown>/g)].map((m) => m[1]);

    if (props.length === 0) {
        declarations.push(`    export const ${name}: () => string;`);
    } else {
        // Use string | number since params can be either (e.g. counts passed as numbers)
        const argsType = props.map((p) => `${p}: string | number`).join('; ');
        declarations.push(`    export const ${name}: (args: { ${argsType} }) => string;`);
    }
}

const output = `// Auto-generated by scripts/generate-paraglide-types.js
// Do not edit manually — changes will be overwritten on the next \`npm run check\`

declare module '$paraglide/messages' {
${declarations.join('\n')}
}

declare module '$paraglide/runtime' {
    export const availableLanguageTags: readonly string[];
    export const sourceLanguageTag: string;
    export function setLanguageTag(tag: string): Promise<void> | void;
}
`;

// Format with the repo's prettier config so the generated file always passes
// `npm run lint` (long declarations would otherwise exceed the print width).
const prettierConfig = await prettier.resolveConfig(outputPath);
const formatted = await prettier.format(output, {
    ...prettierConfig,
    filepath: outputPath,
});

writeFileSync(outputPath, formatted);
console.log(
    `Generated ${declarations.length} paraglide type declarations → src/paraglide.d.ts`
);

if (existsSync(legacySettingsPath)) {
    unlinkSync(legacySettingsPath);
    console.log(
        'Removed legacy src/paraglide-settings-menu.d.ts (now covered by generated file)'
    );
}
